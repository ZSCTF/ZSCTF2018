#include<iostream>
#include<cstdio>
#include<cmath>
using namespace std;
int main()
{
	char cmp[] = { 0x10,0x1A,0x17,0x11,0x0D,0x4F,0x73,0x5D,0x7A,0x70,0x29,0x4E,0x12,0x43,0x47,0x0E,0x29,0x1A,0x42,0x13,0x12,0x29,0x13,0x45,0x07,0x19,0x01,0x28,0x23,0x18,0x0F,0x0B,0};
	int v1, i;
	for (v1 = 23,i=24;;)
	{
		cmp[v1] ^= cmp[i];
		cmp[i] ^= cmp[v1];
		cmp[v1] ^= cmp[i] + 1;
		if (v1 == 17)
			break;
		v1--;
		i++;
	}
	for (i = 15; i >= 11; --i)
	{
		cmp[i]= ((cmp[i] & 0xCC) >> 2) | (4 * cmp[i] & 0xCC);
		cmp[i] ^= 0xe;
	}
	for (i = 9; i >= 5; --i)
	{
		cmp[i]= ((cmp[i] & 0xAA) >> 1) | (2 * cmp[i] & 0xAA);
		cmp[i] ^= 0xad;
	}
	for (i = 31; i >= 0; --i)
	{
		cmp[i] ^= 0x76;
	}
	printf("%s\n", cmp);
	return 0;
}